package de.steinwedel.messagebox;

/**
 * An enumeration to easily address the displayed icon for the dialog.
 * 
 * @author Dieter Steinwedel
 */
public enum Icon {
	
	/**
	 * An icon should be displayed, that indicates an question.
	 */
	QUESTION,
	
	/**
	 * An icon should be displayed, that indicates an info.
	 */
	INFO,
	
	/**
	 * An icon should be displayed, that indicates an warn.
	 */
	WARN,
	
	/**
	 * An icon should be displayed, that indicates an error.
	 */
	ERROR,
	
	/**
	 * No icon should be displayed.
	 */
	NONE
	
}
